#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	j = 0;
	i = 0;
	while (*(s1 + i) != '\0')
		i++;
	while (*s2 != '\0' && j++ < n)
		*(s1 + i++) = *(char *)s2++;
	*(s1 + i++) = '\0';
	return (s1);
}
