#include "libft.h"

int		ft_strccpy(char *des, char *src, char c)
{
	int i;

	i = 0;
	if (!src)
		return (0);
	while (*(src + i) != '\0' && *(src + i) != c)
	{
		*(des + i) = *(src + i);
		i++;
	}
	*(des + i) = '\0';
	return (i);
}
